"""
각 전략은 src/config.py의 STRATEGIES 딕셔너리에서 설정할 수 있습니다:

1. aggressive_scalping (극공격적 단타)
   - stop_loss: 손절 비율
   - take_profit: 익절 비율
   - rsi_oversold: RSI 과매도 기준
   - rsi_overbought: RSI 과매수 기준

2. conservative_scalping (보수적 단타)
   - stop_loss: 손절 비율
   - take_profit: 익절 비율
   - rsi_min/max: RSI 구간

3. mean_reversion (평균 회귀)
   - ma_period: 이동평균 기간
   - deviation_threshold: 이탈 기준

4. grid_trading (그리드 거래)
   - grid_count: 그리드 개수
   - grid_spacing: 그리드 간격
"""


