"""
src/config.py 파일에서 다음을 수정할 수 있습니다:

- INITIAL_CAPITAL: 초기 자본 (기본: 500,000원)
- MAX_DAILY_LOSS: 일일 최대 손실 (기본: 50,000원)
- MAX_CUMULATIVE_LOSS: 누적 최대 손실 (기본: 100,000원)
- MAX_POSITIONS: 최대 동시 포지션 (기본: 3개)
- MAX_POSITION_RATIO: 단일 포지션 최대 비율 (기본: 30%)
- WHITELIST_COINS: 거래할 코인 목록
- STRATEGIES: 각 전략별 파라미터
"""


